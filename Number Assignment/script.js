// Addition Function
function add(a,b){
   return  a+b;
}

// Substraction Function
function sub(a,b){
    return a-b;
}

// Multiplication function
function mult(a,b){
    return a*b;
}

// Division Function
function div(a,b){
    return a/b;
}
console.log(add(5,6));
console.log(sub(5,6));
console.log(mult(5,6));
console.log(div(5,6));